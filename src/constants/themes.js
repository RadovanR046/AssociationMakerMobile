export const DEFAULT_THEME_ID = 'classic';
export const DEFAULT_COLOR_MODE = 'dark';

export function getActiveTheme(settings) {
  const themeId = THEMES[settings.themeId] ? settings.themeId : DEFAULT_THEME_ID;
  const colorMode = settings.colorMode === 'light' ? 'light' : DEFAULT_COLOR_MODE;
  const themeConfig = THEMES[themeId];

  return {
    ...themeConfig.modes[colorMode],
    colorMode,
    themeId,
    themeLabel: themeConfig.label,
  };
}
